#관계 (비교)연산은 두수 의 대소 혹은 등등 관계를 연산해서
#boolean(True, False) 값을 내여주는 연산

a = int(input("첫 번 째 정수:"))
b = int(input("두 번 째 정수:"))

print(a, "<", b, "=>" , (a<b))
print(a, "<=", b, "=>" , (a<=b))
print(a, ">", b, "=>" , (a>b))
print(a, ">=", b, "=>" , (a>=b))
print(a, "==", b, "=>" , (a==b))
print(a, "!=", b, "=>" , (a!=b))
